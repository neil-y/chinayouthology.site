					<p>In 2008 a group of young, geeky explorers who love to ask questions, gathered together to found China Youthology. We are compelled to ask the questions that a new generation are beginning to explore.  Right now Chinese youth are exploring new values, lifestyles, and relationships, and they are searching for support and guidance. Exploring these questions are as much for ourselves as they are for youth. But China Youthology was founded also because we see big opportunity for brands to connect and be more meaningful to youth. We believe brands have an important role to play in youth's lives, and by asking questions with youth, we endeavour to bring brands into relevance. </p>
					<h4>The DNA of China Youthology is Youth and Ology – Research.</h4>
