					            		<dt><strong>2．个人客户变更银行结算账号</strong></dt>
					            		<dd>个人客户在银行柜面办理托管账号的变更。</dd>
					            		<dd>需要按照以下流程办理——个人客户持有效身份证件、新旧银行存折（卡）前往银行网点填写《大宗商品交易资金结算（银商通）业务申请表》。</dd>
					            	</dl>
					            	<dl class="mt-10 mb-15">
					            		<dt><strong>3．个人客户撤销签约</strong></dt>
					            		<dd>个人客户在银行柜面办理解约交易。 客户在需要办理银行账户变更及销户时，需先通过银商转账方式将其在市场方的保证金账户余额清零，客户的保证金账户余额清零后直至重新成功指定结算银行之前不能再发生买卖交易，客户在转账成功后第二个工作日，到乙方办理撤销现有结算银行手续，然后再办理新结算银行的指定手续。</dd>
					            		<dd>办理流程——个人客户本人持有效身份证件、同名银行存折（卡）、前往银行网点填写《大宗商品交易资金结算（银商通）业务申请表》。</dd>
					            	</dl>
